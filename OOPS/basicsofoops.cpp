// Object Oriented Programming in C++(oops)..

// it was introduced to solve the problem by organizing into classes and objects.
// making programs easier to understand, reuse and maintain.

// KEY FEATURES OF OOPS IN CPP:
// 1. CLASS: A class is a user-defined data type that serves as a blueprint for creating objects. 
//           It encapsulates data for the object and methods to manipulate that data.
// 2. OBJECT: An object is an instance of a class. It represents a specific realization of the class with its own unique data.
// 3. ENCAPSULATION: Encapsulation is the bundling of data and methods that operate

// -> strucutres code into logical unit(classes and objects).
// -> keepsrelated data and methods together(encapsulation).
// -> makes code modular, reusable and scalable.
// -> prevent unauthorized access to data(secure).
// -> follows DRY principle(Don't Repeat Yourself).

// CHARACTERSTICS OF OOPS:
// 1. CLASS- a class is a user defined prototype or a blueprint from which objects are created.
//  it represents the set of properties or methods that are common to all objects of one type.
//  Main components of a class:
//   -> access specifiers(public, private, protected)
//   -> class name
//   -> body(it enclosed with braces {} and defines data members and member functions).

// 2. OBJECT- an object is a basic unit of oops that represents real life entities.
// Main components of an object:
//  -> state(attributes or properties) = it is represented by data members of an object.
//  -> member function = collection of statement that perform specific task.
//  -> behavior = it is represented by member functions of an object.
//  -> identity = it gives a unique name to an object.
 
// 3. ENCAPSULATION- it is the process of wrapping data and methods into a single unit called class. diagram--> (data|method)}class.

// 4. INHERITANCE- it is an important pillar of oops by which one class is allow to inherit the features(data member and member functions of another class),
//                 we achieve inheritance by an access specifier(public, private, protected).
//                 it is also known "is-a" relationship..
//                 importance of inheritance:
//                 -> super class
//                 -> subclass
//                 -> reusability of code

// 5. POLYMORPHISM- means many forms..means one entity can allow same method or object to behave diffently based on a context especially on the projects actual runtime class..
//                 TYPES:
//                 -> compile time.
//                        - function overloading
//                        - operator overloading
//                 -> runtime.
//                        - overriding

// 6. ABSTRACTION- it is the process of hiding the implementation details and showing only essential details and features to the users.
